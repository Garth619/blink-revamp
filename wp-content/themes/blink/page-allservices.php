	<h2>Classic Lashes vs Volume Lashes</h2>
	
	<img class="alignleft" width="450" src="<?php bloginfo('template_directory');?>/images/ba-blush-volume-classic.jpg"/>
	
	<p>Choosing the right look for you is important, which is why at Blink Lash Boutique we offer a variety of looks with different degrees of fullness, amount of extensions applied, and style of extensions.  Many people coming in for lash extensions are looking for a lot of fullness or volume, but not everyone has enough natural lashes to achieve the look they want with classic lash extensions.  Sometimes even women who are blessed with thick and full natural lashes want an even fuller look than what classic lashes can give them, and they opt for volume extensions to maximize their fullness.</p>

<p>The difference between classic extensions and volume extension is the number of extensions placed on each natural lash.  Classic lash extensions is the traditional technique of applying eyelash extensions.  It is a ratio of 1 extension to 1 natural lash.  Volume lash extensions, also called Russian volume, is creating a fan of several finer extensions and applying it to one natural lash.  It’s most commonly a ratio of 3-5 lashes to one natural lash. (See photo below)</p>

<p>Typically with classic extensions the extension used is between .10mm to .15mm in diameter.  The extensions used to create the fans for volume application are .05mm or .07mm.  So each individual lash in the volume fan is finer than the individual classic extension.  Classic extensions tend to create more of a mascara look, with each strand thicker. Volume extensions create a fuller and fluffier look and help fill in any gaps in natural lashes.  If fullness is what you’re after and you have finer or sparse natural lashes, volume extensions will help you achieve more fullness than you could with classic extensions.</p>

<p>Since both styles of extensions are attached to each natural lash and not the skin, we are only able to apply as many classic or volume extensions as the number of natural lashes you have.  So the more natural lashes an individual has, the fuller and thicker we can get their extensions.  The finer and more sparse the natural lashes are, the more limited we are in the amount of extensions we can apply.</p>

<p>Since the volume technique requires a lot more skill and training, and the application takes longer, the cost for volume extensions is higher.  Please see pricing below for comparison.</p>

